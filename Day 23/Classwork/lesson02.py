def love_letter(name):
    letter = f"""
ჩემო საყვარელო {name},

მე სიტყვებით ვერ აღვწერ, რამდენად ძვირფასი ხარ ჩემთვის. ყოველი დღე შენთან ერთად საოცარ თავგადასავლად იქცევა. შენი ღიმილი ანათებს ჩემს სამყაროს და მავსებს სიხარულით.  
შენი გულის სითბო და სიკეთე ყველაზე დიდი საჩუქარია, რაც ოდესმე მიმიღია.  
მე მადლიერი ვარ თითოეული წამისთვის, რომელსაც შენთან ერთად ვატარებ და მოუთმენლად ველი ჩვენს საერთო მომავალს.

სიყვარულით,  
{name}
    """
    print(letter)

# ფუნქციის გამოყენება
love_letter("ლუკა")
