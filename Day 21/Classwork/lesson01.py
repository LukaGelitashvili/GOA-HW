# Initial list of numbers
nums = [2, 4, 8, 9, 5]

# Insert the value 3 at index 1
nums.insert(1, 3)


# Remove the first occurrence of the value 9 from the list
nums.remove(9)


# Count how many times the value 8 appears in the list (count = 1)
nums.insert(0, nums.count(8))


# Print the element at index 3 of the final list
print(nums[3])  

#output = 4