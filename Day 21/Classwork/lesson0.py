# Define a list with initial values
list = [8, 4, 2, 6]  

# Remove the first occurrence of the value 2 from the list
list.remove(2)  


# Calculate the length of the list
len_list = len(list)  


# Count how many times the value 6 appears in the list
count_six = list.count(6)  


# Add the length of the list and the count of 6, then print the result
print(len_list + count_six)  


